  String name = "Quynh Anh";
  bool isStudent = true;

  // Print values
  print("Name: $name");
  print("Age: $age");
  print("Height: $height m");
  print("Student: $isStudent");

  // String interpolation with expression
  print("Next year age: ${age + 1}");
}


